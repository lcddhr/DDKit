import UIKit

class DYUIUtils: NSObject {

    static func ScreenWidth() -> CGFloat {
        return UIScreen.mainScreen().bounds.size.width
    }
    
    static func ScreenHight() -> CGFloat {
        return UIScreen.mainScreen().bounds.size.height
    }
}
